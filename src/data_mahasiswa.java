    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        try {
            String nama = txtnama.getText();
            String nim = txtnim.getText();
            String fakultas = txtfakultas.getText();
            String alamat = txtalamat.getText();
            
            pst = con.prepareStatement("insert into data_mahasiswa (nim, nama, fakultas, alamat) values (?,?,?,?)");
            pst.setString(1, nim);
            pst.setString(2, nama);
            pst.setString(3, fakultas);
            pst.setString(4, alamat);
            
            
            int k = pst.executeUpdate();
            
            if (k==1){
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
                txtnim.setText("");
                txtnama.setText("");
                txtfakultas.setText("");
                txtalamat.setText("");
                txtnim.requestFocus();
                LoadMahasiswaID();
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan");
            }
        } catch (SQLException ex) {
            Logger.getLogger(data_mahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String id = txtid.getSelectedItem().toString();
        
        try {
            String nama = txtnama.getText();
            String nim = txtnim.getText();
            String fakultas = txtfakultas.getText();
            String alamat = txtalamat.getText();
            
            pst = con.prepareStatement("update data_mahasiswa set nim=?, nama=?, fakultas=?, alamat=? where id=?");
            pst.setString(1, nim);
            pst.setString(2, nama);
            pst.setString(3, fakultas);
            pst.setString(4, alamat);
            pst.setString(5, id);
            
            int k = pst.executeUpdate();
            
            if (k==1){
                JOptionPane.showMessageDialog(this, "Data Berhasil Diupdate");
                txtnim.setText("");
                txtnama.setText("");
                txtfakultas.setText("");
                txtalamat.setText("");
                txtnim.requestFocus();
                LoadMahasiswaID();
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Data Gagal Diupdate");
            }
        } catch (SQLException ex) {
            Logger.getLogger(data_mahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

