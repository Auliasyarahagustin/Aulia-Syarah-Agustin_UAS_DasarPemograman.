    public void LoadMahasiswaID(){
        try {
            pst = con.prepareStatement("select id from data_mahasiswa");
            rs = pst.executeQuery();
            
            txtid.removeAllItems();
            
            while(rs.next()){
                txtid.addItem(rs.getString(1).toString());
            }
          
            
            
        } catch (SQLException ex) {
            Logger.getLogger(data_mahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
    
